<h1>Game Tips</h1>
<h2>Alternate Controls</h2>
<p>
	Though not as fluid as mouse control, keyboard controls have been implemented. Use the W,A,S,D keys to move your fish around the water. Note that this doesn't disable the mouse control, so you'll need to not touch your mouse during gameplay (else your fish will jump back and forth).
</p>
<h2>Help</h2>
<p>
	In-game help can be triggered by hitting the space key at any time during a level of gameplay.
</p>
<h2>Tactics</h2>
<p>
	There are a number of ways you can approach the game, and the fun is in finding these out for yourself. Still, you're on this page for a reason, so here are a few suggested tactics:
</p>
<ul>
	<li>
		<strong>Become a Predator</strong> - eat plankton to gain XP, spend your XP on growing, and start eating fish that are smaller than you. Be quick- smaller fish will swim away from you! There will always be a few fish that are bigger than you, and as you grow it becomes increasingly difficult to avoid them in the small lake, but this is a good tactic for beginners.
	</li>
	<li>
		<strong>Become a Sneaky Predator</strong> - rather than growing, concentrate your efforts on improving your poison abilities. With poison bought, you can left click anywhere in the water to leave behind a poisonous toxin (the number of toxins you can leave at any one time depends on how much you've upgraded your poison ability). If any fish big or small swims into your poison, they die instantly. You're then able to feast on their blue, floaty carcass. This earns less than half the XP that eating a live fish would give you, but you can tackle fish of any size.
	</li>
	<li>
		<strong>Be Invisible</strong> - unlock camouflage to blend into the sand and no fish will be able to see you. You can't hide forever though- if a fish swims close enough to the sand they'll still be able to eat you. Spend XP on the Murky Water upgrade to increase the levels of plankton in the water, making life easier to gain XP more quickly. When you can afford it, fork out for the Flying Fish ability to leap above the surface of the water and laugh at the water-trapped predators swimming hungrily below you.
	</li>
</ul>
<h2>Achievements</h2>
<p>
	Skillful gameplay unlocks achievements. Achievements on their own seem a little "hollow", but fear not, there are some nice surprises in store. Click on achievements you have earned to display a message at the bottom of the screen- some of your achievements will lead to <em>cheat codes</em>!
</p>
<h2>Cheat Codes</h2>
<p>
	Cheat codes can be used at any time during gameplay, though you might want to pause the game first by hitting the spacebar, giving you time to enter cheat codes without having to worry about other fish. Cheat codes are a series of characters that, when entered, do some amazing things. They can make you grow instantaneously, give you huge amounts of XP, and even let you skip entire levels.
</p>
<p>
	Again, cheat codes are satisfying when you <em>find them out for yourself</em>, but if you're too lazy to do that, here is a complete list:
</p>
<ul>
	<li>
		<code>*xp</code> - gain 1000XP
	</li>
	<li>
		<code>*end</code> - end current level instantly
	</li>
	<li>
		<code>*grow</code> - grow to your full size
	</li>
	<li>
		<code>*camo</code> - gain the camouflage ability
	</li>
	<li>
		<code>*fly</code> - gain the flying fish ability
	</li>
</ul>
	
<p>
	Struggling to get it working? Make sure you're entering the asterisk (*) before the rest of the code. For example, <code>*xp</code> will work, <code>xp</code> will not. Also, <em>don't hold shift for the rest of the code</em>. You need to hold shift to make the * character, but make sure you release the shift key before entering the rest of the cheat code. Finally, you need to enter the sequence within a certain time frame- don't dilly dally, or your sequence will not be recognised!
</p>
